#This is the tank size of the car, of how much fuel it can hold
tank_size = 15

#This is the distance that the car is driving
distance = 600

#This is an equation for miles per gallon
#This takes the tank size and the distance
#The distance divides the tank size and that is how you get the miles per gallons
mpg = distance / tank_size

#This will state the words 'miles per gallon' 
#Along with the number from the equation
print("Miles per gallon:", mpg)